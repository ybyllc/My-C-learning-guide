#include<stdio.h>

int main()
{
    int a[3][3];
    double b[3][3];
    double x1,x2;
    int sum;

    printf("请输入三阶行列式：\n");
    scanf("%d%d%d",&a[0][0],&a[0][1],&a[0][2]);
    scanf("%d%d%d",&a[1][0],&a[1][1],&a[1][2]);
    scanf("%d%d%d",&a[2][0],&a[2][1],&a[2][2]);
    b[0][0] = a[0][0];
    b[0][1] = a[0][1];
    b[0][2] = a[0][2];

    x1=(double)a[1][0]/a[0][0];
    b[1][0] = a[1][0]-x1*a[0][0];
    b[1][1] = a[1][1]-x1*a[0][1];
    b[1][2] = a[1][2]-x1*a[0][2];

    x2 = (double)a[2][0]/a[0][0];
    b[2][0] = a[2][0]-x2*a[0][0];
    b[2][1] = a[2][1]-x2*a[0][1];
    b[2][2] = a[2][2]-x2*a[0][2];
    
    x2 = (double)b[2][1]/b[1][1];
    b[2][0] = b[2][0]-x2*b[1][0];
    b[2][1] = b[2][1]-x2*b[1][1];
    b[2][2] = b[2][2]-x2*b[1][2];

    printf("\n");
    printf("%2.0f  %2.0f  %2.0f\n",b[0][0],b[0][1], b[0][2]);
    printf("%2.0f  %2.0f  %2.0f\n",b[1][0],b[1][1], b[1][2]);
    printf("%2.0f  %2.0f  %2.0f\n",b[2][0],b[2][1], b[2][2]);
    sum=b[0][0]*b[1][1]*b[2][2];
    printf("结果为：%d",sum);
    
    return 0;
}