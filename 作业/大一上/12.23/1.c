/*
【问题描述】输出一张华氏与摄氏温度对照表，将华氏温度从30°～40°F之间的每度转换对应的摄氏温度输出。华氏转化为摄氏温公式：c=5/9(F－32) 
c=5/9(F－32) 
【输入形式】无
【输出形式】华氏，摄氏（保留3位小数）
【样例输入】

无
【样例输出】

30,-1.111
31,-0.556
32,0.000
33,0.556
34,1.111
35,1.667
36,2.222
37,2.778
38,3.333
39,3.889
40,4.444
*/
#include<stdio.h>

int main()
{
    int F;
    float c;
    for (F = 30; F <= 40;F++)
    {
        c=5.0/9*(F-32); 
        printf("%d,%.3f\n",F,c);
    }
    return 0;
}